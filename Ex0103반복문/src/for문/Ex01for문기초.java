package for문;

public class Ex01for문기초 {

	public static void main(String[] args) {

		// for문 구조
		// 1부터 10까지 출력
		// for(초기화 구문; 조건 구문; 출력 후 작업) { 반복할 구문 }
		// 적용순서 
		//초기화 구문 -> 조건 구문 -> 반복할 구문 -> 출력 후 작업
		// -> 조건 구문 -> 반복할 구문
		for (int i = 1; i <= 10; i++) {
			System.out.print(i+ " ");
		}

	}

}
